    $filters = Invoke-Command -ComputerName $node.Name -ScriptBlock { fltmc filters | Select-String -Pattern '^[^\s]+' }

    # Process each filter
    foreach ($filter in $filters) {
        $filterName = $filter -replace '\s.*', ''
        $match = $false
        $company = 'Unknown'
        
        foreach ($data in $filterData) {
            if ($filterName -eq $data.FilterName) {
                $match = $true
                $company = $data.Company
                break
            }
        }
        
